#include <iostream>
#include <vector>
#include <algorithm>
#include <climits>

using namespace std;

int main() {
    // Вхідні дані
    vector<int> A = {10, -5, 20, 3, -8, 0, 15, 7, -2};
    int n = A.size();

    cout << "Початковий масив: ";
    for (int x : A) cout << x << " ";
    cout << endl << "----------------" << endl;

    // ==========================================
    // ЗАДАЧА 1: Знайти індекс першого входження P
    // ==========================================
    int P;
    cout << "Введіть число P для пошуку: ";
    cin >> P;

    int foundIndex = -1; // -1 означає, що не знайшли

    // Проходимо по масиву
    for (int i = 0; i < n; ++i) {
        if (A[i] == P) {
            foundIndex = i;
            break; // Зупиняємо цикл, бо нам потрібне ПЕРШЕ входження
        }
    }

    if (foundIndex != -1) {
        cout << "Задача 1: Число " << P << " знаходиться під індексом " << foundIndex << endl;
    } else {
        cout << "Задача 1: Число " << P << " не знайдено в масиві." << endl;
    }



    cout << "----------------" << endl;

    // ==========================================
    // ЗАДАЧА 2: Найменше серед ДОДАТНИХ елементів
    // ==========================================
    int minPositive = INT_MAX; // Ініціалізуємо максимальним можливим числом
    bool foundPos = false;     // Прапорець, чи є взагалі додатні числа

    for (int x : A) {
        if (x > 0) { // Перевіряємо, чи число додатне
            if (x < minPositive) {
                minPositive = x;
                foundPos = true;
            }
        }
    }

    if (foundPos) {
        cout << "Задача 2: Найменше додатне число: " << minPositive << endl;
    } else {
        cout << "Задача 2: У масиві немає додатних чисел." << endl;
    }

    cout << "----------------" << endl;

    // ==========================================
    // ЗАДАЧА 3: Поміняти місцями найбільше і найменше
    // ==========================================
    if (n > 0) {
        int minIdx = 0;
        int maxIdx = 0;

        // Шукаємо індекси мінімуму та максимуму
        for (int i = 1; i < n; ++i) {
            if (A[i] < A[minIdx]) {
                minIdx = i;
            }
            if (A[i] > A[maxIdx]) {
                maxIdx = i;
            }
        }

        cout << "Задача 3: Мін (" << A[minIdx] << ") та Макс (" << A[maxIdx] << ") поміняні місцями." << endl;

        // Міняємо місцями елементи за знайденими індексами
        std::swap(A[minIdx], A[maxIdx]);

        cout << "Масив після заміни: ";
        for (int x : A) cout << x << " ";
        cout << endl;
    } else {
        cout << "Масив порожній." << endl;
    }

    return 0;
}